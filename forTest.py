def sort(mas):
    #  сортировка массива со вычислительной сложностью алгоритма — O(n2)
    for i in range(len(mas)):
        _min = i 
        for j in range(i, len(mas)):
            if mas[j] < mas[_min]: _min = j
        temp = mas[i]
        mas[i] = mas[_min]
        mas[_min] = temp
    return mas

